-------------------------------------------------------------------------------------------------------
-- TABLE SUCURSAL
CREATE TABLE SUCURSAL (
  S_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  S_NOMBRE VARCHAR2(50) NOT NULL,
  S_CIUDAD VARCHAR2(50) NOT NULL,
  S_CALLE VARCHAR2(50) NOT NULL
);
-------------------------------------------------------------------------------------------------------
-- TABLE CLIENTE
CREATE TABLE CLIENTE (
  CLIENTE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  S_ID NUMBER NOT NULL,
  CLIENTE_TDOCUM VARCHAR2(10) NOT NULL,
  CLIENTE_DOCUM VARCHAR2(50) NOT NULL,
  CLIENTE_PNOMBRE VARCHAR2(50) NOT NULL,
  CLIENTE_SNOMBRE VARCHAR2(50),
  CLIENTE_PAPELLIDO VARCHAR2(50) NOT NULL,
  CLIENTE_SAPELLIDO VARCHAR2(50) NOT NULL,
  CLIENTE_EMAIL NVARCHAR2(50) NOT NULL,
  CLIENTE_CIUDAD VARCHAR2(50) NOT NULL,
  CLIENTE_CALLE VARCHAR2(50) NOT NULL,
  CLIENTE_NUMERO VARCHAR2(50) NOT NULL,
  FOREIGN KEY (S_ID) REFERENCES SUCURSAL(S_ID)
);
-------------------------------------------------------------------------------------------------------
-- TABLE CUENTA
CREATE TABLE CUENTA (
  CUENTA_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  CLIENTE_ID NUMBER NOT NULL,
  S_ID NUMBER NOT NULL,
  PROD_ID NUMBER NOT NULL,
  CUENTA_NUMERO NUMBER NOT NULL UNIQUE,
  CUENTA_ESTADO VARCHAR2(10) NOT NULL,
  CUENTA_MONTO NUMBER(20,2) NOT NULL,
  CUENTA_TIPO VARCHAR2(10) NOT NULL,
  CUENTA_FECHA DATE NOT NULL,
  FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTE(CLIENTE_ID),
  FOREIGN KEY (S_ID) REFERENCES SUCURSAL(S_ID),
  FOREIGN KEY (PROD_ID) REFERENCES PRODUCTO(PROD_ID)
);
-------------------------------------------------------------------------------------------------------
-- TABLE TRANSACCIONES 
CREATE TABLE TRANSACCIONES (
  T_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  CUENTA_ID NUMBER NOT NULL,
  T_TIPO VARCHAR2(50) NOT NULL,
  T_CANTIDAD NUMBER NOT NULL,
  T_FECHA DATE NOT NULL,
  FOREIGN KEY (CUENTA_ID) REFERENCES CUENTA(CUENTA_ID)
);
-------------------------------------------------------------------------------------------------------
-- TABLE PRODUCTO
CREATE TABLE PRODUCTO (
  PROD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  PROD_NOMBRE VARCHAR2(5),
  PROD_INTERES NUMBER(5,2) NOT NULL
);